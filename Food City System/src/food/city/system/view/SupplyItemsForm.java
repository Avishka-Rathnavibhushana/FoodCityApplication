    private void removeItemBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeItemBtnActionPerformed
        if (supplyItemInfoTable.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Select an Item first");
            return;
        }
        float amount = (float) supplyItemInfoTable.getValueAt(supplyItemInfoTable.getSelectedRow(), 4);
        tot = tot - amount;
        totalAmounttxt.setText(tot+"");
        DefaultTableModel dtm=(DefaultTableModel) supplyItemInfoTable.getModel();

        dtm.removeRow(supplyItemInfoTable.getSelectedRow());
        ItemIdtxt.requestFocus();
    }//GEN-LAST:event_removeItemBtnActionPerformed

