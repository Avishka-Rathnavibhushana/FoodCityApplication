    private void saveSupplyFormBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveSupplyFormBtnActionPerformed

        String supplierId= supplierIdTxt.getText();
        String date = SupplyFormDatetxt.getText();
        String supplierName = supplierNameTxt.getText();
        if (supplierId.equals("")) {
            supplierIdTxt.requestFocus();
            JOptionPane.showMessageDialog(this, "Fill Supplier Name.");
            return;
        }

        ArrayList<ItemDTO> itemDetailList=new ArrayList<>();

        DefaultTableModel dtm=(DefaultTableModel) supplyItemInfoTable.getModel();

        if (dtm.getRowCount()==0) {
            JOptionPane.showMessageDialog(this, "Add items first.");
            return;
        }

        for (int i = 0; i < dtm.getRowCount(); i++) {
            String itemID= (String) dtm.getValueAt(i, 0);
            int qty=(int) dtm.getValueAt(i, 2);
            float unitPrice=(float) dtm.getValueAt(i, 3);

            ItemDTO itemDTO = new ItemDTO(itemID, "", unitPrice, 0, qty);
            itemDetailList.add(itemDTO);
        }

        SupplyDeteilDTO supplyDeteilDTO = new SupplyDeteilDTO(supplierId, date, itemDetailList, tot);

        try {
            boolean isAdded = supplyItemFormController.saveSupplyItems(supplyDeteilDTO);
            if(isAdded){
                JOptionPane.showMessageDialog(this, "Added Success");
                dtm.setRowCount(0);
                supplierNameTxt.setText("");
                totalAmounttxt.setText("");
                supplierIdTxt.setText("");
                supplierIdTxt.requestFocus();
                tot = 0;
            }
        } catch (Exception ex) {
            Logger.getLogger(BillGenerator.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveSupplyFormBtnActionPerformed

