    private void employeeIdtxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_employeeIdtxtActionPerformed
        try {
            String Eid = employeeIdtxt.getText();
            EmployeeDTO employeeDTO = employeePaysheetController.getEmployee(Eid);

            if (employeeDTO==null) {
                JOptionPane.showMessageDialog(this, "Insert valid employee ID");
                return;
            }
            
            if (employeePaysheetController.searchEmployee(Eid)) {
                JOptionPane.showMessageDialog(this, "Paysheet has already created for this employee");
                return;
            }
            
            employeeNametxt.setText(employeeDTO.getName());
            fixedSalarytxt.setText(employeeDTO.getSalary()+"");
            totalSlaryTxt.setText(employeeDTO.getSalary()+"");
            otRatetxt.requestFocus();
        } catch (Exception ex) {
            Logger.getLogger(EmployeePaysheet.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_employeeIdtxtActionPerformed

