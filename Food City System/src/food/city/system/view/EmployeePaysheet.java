    private void savePaysheetBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savePaysheetBtnActionPerformed
        
        String Eid = employeeIdtxt.getText();
        if (Eid.equals("")||otRatetxt.getText().equals("")||otHourstxt.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Fill empty fields");
            return;
        }
        float otRate = Float.parseFloat(otRatetxt.getText());
        float otHours = Float.parseFloat(otHourstxt.getText());
        float fixedSal = Float.parseFloat(fixedSalarytxt.getText());
        float otAmount = Float.parseFloat(totalOTAmount.getText());
        float totalSalary = fixedSal+otAmount;
        String date = LocalDate.now().toString();
        
        
        PaysheetDTO paysheetDTO = new PaysheetDTO(Eid, fixedSal, otRate, otHours, otAmount, totalSalary, date);
        
        try {
            boolean success = employeePaysheetController.savePaysheet(paysheetDTO);
            if (success) {
                JOptionPane.showMessageDialog(this, "Paysheet Successfully added");
            }else{
                JOptionPane.showMessageDialog(this, "Paysheet already created.");
            }
        } catch (Exception ex) {
            Logger.getLogger(EmployeePaysheet.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        clearFields();
        employeeIdtxt.requestFocus();;
    }//GEN-LAST:event_savePaysheetBtnActionPerformed

